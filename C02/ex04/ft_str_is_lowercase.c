int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 95 && str[i] <= 122))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
